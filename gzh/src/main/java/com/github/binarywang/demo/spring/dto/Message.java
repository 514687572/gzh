package com.github.binarywang.demo.spring.dto;

public class Message {
	public String text;
	public String code;
	
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
}
